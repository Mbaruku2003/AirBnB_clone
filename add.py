#!/usr/bin/python3
"""Module for a simple add function."""


def add(a=0, b=1):
    """Return the sum of a and b.

    Args:
    a (int, float): the first number. Default is 0.
    b (int, float): The second number.Default is 1.

    Returns:
    int, float: The sum of a and b.
    """

    return a + b
